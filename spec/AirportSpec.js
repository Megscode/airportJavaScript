describe("Airport", function() {
  
  it('should let a plane land', function(){
    var plane = new Plane(); 
    var airport = new Airport();
    expect(airport.land(plane)).toEqual[plane]
  });

  it('plane should have taken off', function(){
    var plane = new Plane(); 
    var airport = new Airport();
    expect(airport.land(plane)).toEqual[plane]
  });

});